// Check number Positive, Negative or Zero:

#include<bits/stdc++.h>
using namespace std;

int main() {
    int num = 0;
    if(num > 0)
        cout << "It is Positive Number";
    else if(num < 0) 
        cout << "It is Negative Number";
    else 
        cout << "It is zero Number";
    return 0;
}